package com.woowahan.baeminWaiting004.model;

public class SignUpJsonObject {

	private String userId, userPassword, userMemberTel, userName;
	private int userMemberRole;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserMemberTel() {
		return userMemberTel;
	}
	public void setUserMemberTel(String userMemberTel) {
		this.userMemberTel = userMemberTel;
	}
	public int getUserMemberRole() {
		return userMemberRole;
	}
	public void setUserMemberRole(int userMemberRole) {
		this.userMemberRole = userMemberRole;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
}
